package com.practice.immutable;

public final class Student {

	private final int rollNumber;
	private String name;

	public String getName() {
		return name;
	}
	public int getRollNumber() {
		return rollNumber;
	}
	public Student(int rollNumber, String name) {
		super();
		this.rollNumber = rollNumber;
		this.name = name;
	}
	
	
	
	
	
	
	
	
	
}
